class TroopConfig:
    def __init__(self, display_name, name, cost, health, offense, defense, speed, upkeep, description):
        self.display_name = display_name
        self.name = name
        self.cost = cost
        self.health = health
        self.offense = offense
        self.defense = defense
        self.speed = speed
        self.upkeep = upkeep
        self.description = description